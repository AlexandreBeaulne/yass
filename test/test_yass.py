                       ['4', '0', '2', '7', '0', '6', '0', '0', '0'],
                       ['3', '0', '1', '0', '0', '7', '0', '4', '0'],
                       ['7', '2', '0', '0', '4', '0', '0', '6', '0'],
                       ['0', '0', '4', '0', '1', '0', '0', '0', '3'],
